#include<bits/stdc++.h>
using namespace std;
int main()
{
	string str,temp="";
	cin>>str;
	for(int i=0;i<str.size();i++)
	{
	    if(tolower(str[i])=='a' || tolower(str[i])=='e'||tolower(str[i])=='i'||tolower(str[i])=='i'||tolower(str[i])=='o'||tolower(str[i])=='u')
	    continue;
	    temp+=tolower(str[i]);
	}
	for(int i=0;i<temp.size();i++)
	{
		cout<<'.'<<temp[i];
	}
}
